const dictionaries: Record<Lang, Dict> = { en, "pt-BR": ptBR, es };

export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
    const table = dictionaries[lang] ?? en;
    let value = table[key] ?? en[key] ?? key;
    if (vars) {
        for (const [name, replacement] of Object.entries(vars)) {
            value = value.replaceAll(`{${name}}`, String(replacement));
        }
    }
    return value;
}

export type TranslateFn = (key: string, vars?: Record<string, string | number>) => string;

const LangContext = createContext<Lang>(defaultLang);

export function LangProvider({ lang, children }: Readonly<{ lang: Lang; children: ReactNode }>) {
    return <LangContext.Provider value={lang}>{children}</LangContext.Provider>;
}

export function useT(): TranslateFn {
    const lang = useContext(LangContext);
    return (key, vars) => translate(lang, key, vars);
}

export function useLang(): Lang {
    return useContext(LangContext);
}
